int counter = 50;  // Thời gian quét 0.5 giây (dựa trên cấu hình Timer)
int display = 0;   // Để kiểm tra xem đang hiển thị LED bảy đoạn nào

// Bản đồ segment cho các số từ 0 đến 9 (Common Anode)
uint8_t segmentMap[10] = {
    0x3F, // 0
    0x06, // 1
    0x5B, // 2
    0x4F, // 3
    0x66, // 4
    0x6D, // 5
    0x7D, // 6
    0x07, // 7
    0x7F, // 8
    0x6F  // 9
};

// Hàm để hiển thị số lên LED bảy đoạn
void display7SEG(int num) {
    for (int i = 0; i < 7; i++) {
        // Đảo ngược logic: GPIO_PIN_RESET bật LED, GPIO_PIN_SET tắt LED
        HAL_GPIO_WritePin(GPIOB, (1 << i), (segmentMap[num] & (1 << i)) ? GPIO_PIN_RESET : GPIO_PIN_SET);
    }
}

// Hàm xử lý khi Timer2 hết thời gian
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM2) {
        counter--;
        if (counter <= 0) {
            counter = 50; // Reset counter để tạo ra chu kỳ 0.5 giây

            // Tắt tất cả các LED bảy đoạn trước khi hiển thị số mới
            HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_SET);   // Tắt EN0 (PNP transistor)
            HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_SET);   // Tắt EN1 (PNP transistor)

            if (display == 0) {
                // Hiển thị "1" trên LED bảy đoạn thứ nhất
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET); // Kích hoạt EN0 (PNP transistor)
                display7SEG(1); // Hiển thị số 1
                display = 1;    // Chuyển sang LED thứ hai ở lần tiếp theo

                // Kích hoạt LED đỏ
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET); // Bật LED đỏ
            } else {
                // Hiển thị "2" trên LED bảy đoạn thứ hai
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_7, GPIO_PIN_RESET); // Kích hoạt EN1 (PNP transistor)
                display7SEG(2); // Hiển thị số 2
                display = 0;    // Chuyển về LED thứ nhất ở lần tiếp theo

                // Tắt LED đỏ
                HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET); // Tắt LED đỏ
            }
        }
    }
}

